package de.four.in.a.row;

/*
 * A Class for a coordinate
 *
 * @author Martin Moors
 */
public class Coordinate {
    protected int x;
    protected int y;

    public Coordinate() {
        super();
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }


}